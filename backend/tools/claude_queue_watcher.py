def ensure_directories():
    """Ensure all required directories exist"""
    paths = [
        QUEUE_INPUT_PATH,
        QUEUE_OUTPUT_PATH,
        QUEUE_PROCESSING_PATH,
        QUEUE_ERROR_PATH
    ]

    for path in paths:
        Path(path).mkdir(parents=True, exist_ok=True)

    logger.info("‚úÖ All queue directories ensured")


